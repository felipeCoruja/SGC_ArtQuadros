    private boolean isCamposVazios() {
        boolean flag = false;
        
        if(this.edtReferencia.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Referência' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtCor.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Cor' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtMaterial.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Material' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtPrecoCusto.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Preço Custo' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtPrecoVenda.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Preço Venda' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtComprimentoVara.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Comprimento Vara' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.edtLarguraVara.getText().isEmpty()){
            flag = true;
            JOptionPane.showMessageDialog(null, "O Campo 'Largura Vara' está vazio!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.spinerQtd.getValue().equals(0)){
            flag = true;
            JOptionPane.showMessageDialog(null, "Insira um valor para 'Quantidade Varas' !", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else if(this.cboxFornecedor.getSelectedItem().equals("")){
            flag = true;
            JOptionPane.showMessageDialog(null, "Selecione um 'Fornecedor' para esse cadastro !", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
        
        return flag;
    }
    
    private String formatoStringDouble(String str){
        Pattern pattern = Pattern.compile("[.^]");
        Matcher matcher = pattern.matcher(str);
        if(matcher.find()){
            str = str.replace(".", "");
        }
        
        str = str.replace(",", ".");
        
        return str;
    }
    
    private void limparCampos(){
        this.edtReferencia.setText("");
        this.edtCor.setText("");
        this.edtMaterial.setText("");
        this.edtPrecoCusto.setText("");
        this.edtPrecoVenda.setText("");
        this.edtComprimentoVara.setText("");
        this.edtLarguraVara.setText("");
        this.spinerQtd.setValue(0);
        this.edtDescricao.setText("");
        this.cboxFornecedor.setSelectedItem(1);
    
    }
    
    
    
    private void loadTabela() throws ClassNotFoundException{
        DefaultTableModel modelTable = (DefaultTableModel) this.tabela.getModel();
        modelTable.setNumRows(0);
        
        if(this.listaMolduras != null){
            for(Moldura m: this.listaMolduras){
                modelTable.addRow(new Object[]{
                    m.getId(),
                    m.getCor(),
                    m.getMareial(),
                    m.getQuantMetros(),
                    m.getPrecoCusto(),
                    m.getPrecoVenda(),
                    m.getDescricao()
                });
            }
        }
        
        
    }
    
    private String[] listaFornecedor(){
        FornecedorDAO dao = new FornecedorDAO();
        String[] list = null;
        try {
            list = dao.getNomeFornecedor();
            if(list.length <1 ){//Sem ter o registro padrao "Não"
                JOptionPane.showMessageDialog(null, "ERRO - O sistema não achou Fornecedores registrados no Banco de Dados ");
                String aux = "Sem Registro;";
                list = aux.split(";");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CadMoldura.class.getName()).log(Level.SEVERE, null, ex);
        }
        return list;
    }
    
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(!isCamposVazios()){
            Moldura m = new Moldura();
            String aux = "";
            
            m.setId(this.edtReferencia.getText());
            m.setCor(this.edtCor.getText());
            m.setMareial(this.edtMaterial.getText());
            
            aux = formatoStringDouble(this.edtPrecoCusto.getText());
            m.setPrecoCusto(Double.parseDouble(aux));
            
            aux = formatoStringDouble(this.edtPrecoVenda.getText());
            m.setPrecoVenda(Double.parseDouble(aux));
           
            aux = formatoStringDouble(this.edtComprimentoVara.getText());
            m.setComprimentoVara(Double.parseDouble(aux));

            aux = formatoStringDouble(this.edtLarguraVara.getText());
            m.setLarguraVara(Double.parseDouble(aux));
            
            aux = formatoStringDouble(this.spinerQtd.getValue().toString());
            m.setQuantMetros(Double.parseDouble(aux));
            
            m.setDescricao(this.edtDescricao.getText());
            
            FornecedorDAO fDao = new FornecedorDAO();
            try {
                m.setFornecedorCnpj(fDao.getCnpj(this.cboxFornecedor.getSelectedItem().toString()));
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(CadMoldura.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "ERRO CadMoldura - "+ex);
            }
            
            MolduraDAO dao = new MolduraDAO();
            try {
                
                dao.salvar(m);
                this.listaMolduras = dao.load();
                
                this.loadTabela();
                this.limparCampos();
                JOptionPane.showMessageDialog(null, "Salvo com Sucesso!");
                
                
                
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao Cadastrar moldura em CadMoldura :"+ex);
                Logger.getLogger(CadMoldura.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

